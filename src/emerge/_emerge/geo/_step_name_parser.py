def parse_step_names(filepath: str | Path) -> dict[str, list[int]]:
    """Parse a STEP file and return ``{product_name: [volume_indices]}``.

    Volume indices are 0-based and ordered to match the expected
    GMSH/OCC import order (by entity-id order within the STEP file).

    Handles both simple multi-body part files and assembly files with
    NEXT_ASSEMBLY_USAGE_OCCURRENCE instances.

    Args:
        filepath: Path to the ``.step`` / ``.stp`` file.

    Returns:
        Dictionary mapping product name strings to lists of 0-based
        volume indices.  The union of all lists covers
        ``range(total_volumes)``.
    """
    filepath = Path(filepath)
    text = filepath.read_text(encoding='utf-8', errors='replace')
    entities = _parse_step_entities(text)
    idx = _StepIndex(entities)

    # Collect all products
    products: dict[int, str] = {}  # product_eid -> name
    for eid in idx.by_type.get('PRODUCT', {}):
        products[eid] = idx.product_name(eid)

    # Build product_eid -> product_definition_eid mapping
    prod_to_pd: dict[int, int] = {}
    for prod_eid in products:
        pd = idx.product_to_pd(prod_eid)
        if pd is not None:
            prod_to_pd[prod_eid] = pd

    # Build product_definition -> product_eid reverse mapping
    pd_to_prod: dict[int, int] = {v: k for k, v in prod_to_pd.items()}

    # For each product, find its SHAPE_REPRESENTATION and count its solids
    prod_sr: dict[int, int | None] = {}       # product_eid -> sr_eid
    prod_solids: dict[int, list[int]] = {}     # product_eid -> [solid_eids]
    for prod_eid, pd_eid in prod_to_pd.items():
        sr = idx.pd_to_sr(pd_eid)
        prod_sr[prod_eid] = sr
        if sr is not None:
            prod_solids[prod_eid] = idx.sr_all_solids(sr)
        else:
            prod_solids[prod_eid] = []

    # Check for assembly structure via NEXT_ASSEMBLY_USAGE_OCCURRENCE
    all_nauo = idx.assembly_instances()
    is_assembly = len(all_nauo) > 0

    if is_assembly:
        return _resolve_assembly(idx, products, prod_to_pd, pd_to_prod,
                                 prod_solids, all_nauo)
    else:
        return _resolve_simple(idx, products, prod_to_pd, prod_solids)


def _resolve_simple(
    idx: _StepIndex,
    products: dict[int, str],
    prod_to_pd: dict[int, int],
    prod_solids: dict[int, list[int]],
) -> dict[str, list[int]]:
    """Resolve volume names for a simple (non-assembly) STEP file.

    Each product may own one or more MANIFOLD_SOLID_BREPs.
    We order volumes by the solid entity id within the file.
    """
    # Build flat list: (solid_eid, product_name)
    entries: list[tuple[int, str]] = []
    for prod_eid in sorted(products.keys()):
        name = products[prod_eid]
        for solid_eid in sorted(prod_solids.get(prod_eid, [])):
            entries.append((solid_eid, name))

    # Sort by solid entity id (approximates STEP file order = OCC import order)
    entries.sort(key=lambda x: x[0])

    mapping: dict[str, list[int]] = {}
    for vol_idx, (_, name) in enumerate(entries):
        mapping.setdefault(name, []).append(vol_idx)
    return mapping


def _resolve_assembly(
    idx: _StepIndex,
    products: dict[int, str],
    prod_to_pd: dict[int, int],
    pd_to_prod: dict[int, int],
    prod_solids: dict[int, list[int]],
    all_nauo: list[tuple[int, int, int]],
) -> dict[str, list[int]]:
    """Resolve volume names for an assembly STEP file.

    Each NEXT_ASSEMBLY_USAGE_OCCURRENCE is one placed instance of a child
    product.  The child product owns N solids, so each NAUO contributes
    N volumes.  We order instances by NAUO entity id.
    """
    # Sort NAUO by entity id to approximate file/import order
    sorted_nauo = sorted(all_nauo, key=lambda x: x[0])

    # Build flat volume list
    mapping: dict[str, list[int]] = {}
    vol_idx = 0

    for nauo_eid, parent_pd, child_pd in sorted_nauo:
        child_prod = pd_to_prod.get(child_pd)
        if child_prod is None:
            # Try harder: reverse-trace from PD to PRODUCT
            child_prod = idx.pd_to_product_eid(child_pd)

        if child_prod is not None:
            child_name = products.get(child_prod, f'Unknown_{child_pd}')
            n_solids = len(prod_solids.get(child_prod, []))
        else:
            child_name = f'Unknown_{child_pd}'
            n_solids = 0

        # If we couldn't find any solids via the chain, assume 1
        # (OCC will still import *something* for each NAUO)
        if n_solids == 0:
            n_solids = 1

        for _ in range(n_solids):
            mapping.setdefault(child_name, []).append(vol_idx)
            vol_idx += 1

    return mapping


def get_volume_names(filepath: str | Path) -> list[str]:
    """Return a flat list of product names, one per volume, in import order.

    If a product appears multiple times (assembly instances or multiple
    solids), its name is repeated.

    Example::

        ['Housing', '039_POGO', '039_POGO', ..., '039_POGO']

    Args:
        filepath: Path to the STEP file.

    Returns:
        List of name strings, length equals the expected number of
        GMSH/OCC volumes.
    """
    mapping = parse_step_names(filepath)
    # Flatten back into ordered list
    total = sum(len(v) for v in mapping.values())
    names: list[str | None] = [None] * total
    for name, indices in mapping.items():
        for i in indices:
            names[i] = name
    # Fill any gaps (shouldn't happen, but be safe)
    return [n if n is not None else f'Unknown_{i}' for i, n in enumerate(names)]